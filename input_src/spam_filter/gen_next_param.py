def gen_dotProduct_iN_func(N):
    func_str_list = []
    func_str_list.append('#include "../host/typedefs.h"')
    func_str_list.append('')
    func_str_list.append('')
    func_str_list.append('void dotProduct_i' + str(N) + '(')
    func_str_list.append('  hls::stream<ap_uint<64> > & Input_1, // from data_in_redir')
    func_str_list.append('  hls::stream<ap_uint<32> > & Input_2, // from sigmoid')
    func_str_list.append('  hls::stream<ap_uint<32> > & Output_1, // to sigmoid')
    func_str_list.append('  hls::stream<ap_uint<64> > & Output_2 // to data_2_1')
    func_str_list.append(')')
    func_str_list.append('{')
    func_str_list.append('')
    func_str_list.append('#pragma HLS INTERFACE axis register port=Input_1')
    func_str_list.append('#pragma HLS INTERFACE axis register port=Input_2')
    func_str_list.append('#pragma HLS INTERFACE axis register port=Output_1')
    func_str_list.append('#pragma HLS INTERFACE axis register port=Output_2')
    func_str_list.append('')
    func_str_list.append('')
    func_str_list.append('  const int unroll_factor = PAR_FACTOR_DEC;')
    func_str_list.append('  static FeatureType param[NUM_FEATURES/NUM_OPS];')
    func_str_list.append('  FeatureType grad[NUM_FEATURES/NUM_OPS];')
    func_str_list.append('  static DataType feature[NUM_FEATURES/NUM_OPS];')
    func_str_list.append('  FeatureType scale;')
    func_str_list.append('  FeatureType prob;')
    func_str_list.append('  #pragma HLS array_partition variable=param cyclic factor=unroll_factor')
    func_str_list.append('  #pragma HLS array_partition variable=feature cyclic factor=unroll_factor')
    func_str_list.append('  #pragma HLS array_partition variable=grad cyclic factor=unroll_factor')
    func_str_list.append('  static int odd_even = 0;')
    func_str_list.append('  static int num_train = 0;')
    func_str_list.append('  static int epoch;')
    func_str_list.append('  static int sb = 0;')
    func_str_list.append('  static LabelType training_label;')
    func_str_list.append('  bit64 in_tmp;')
    func_str_list.append('  bit32 out_tmp;')
    func_str_list.append('')
    func_str_list.append('  if(odd_even == 0){')
    func_str_list.append('    in_tmp = Input_1.read();')
    func_str_list.append('    training_label(7,0) = in_tmp(7,0);')
    func_str_list.append('')
    func_str_list.append('   READ_TRAINING_DATA: for (int i = 0; i < NUM_FEATURES / D_VECTOR_SIZE / NUM_OPS; i ++ ){')
    func_str_list.append('   //                                      1024           4')
    func_str_list.append('#pragma HLS PIPELINE II=1')
    func_str_list.append('  VectorFeatureType tmp_data;')
    func_str_list.append('  tmp_data = Input_1.read();')
    func_str_list.append('   READ_TRAINING_DATA_INNER: for (int j = 0; j < D_VECTOR_SIZE; j ++ ){')
    func_str_list.append('    feature[i * D_VECTOR_SIZE + j](DTYPE_TWIDTH-1, 0) = tmp_data((j+1)*DTYPE_TWIDTH-1, j*DTYPE_TWIDTH);')
    func_str_list.append('   }')
    func_str_list.append('   }')
    func_str_list.append('')
    func_str_list.append('   FeatureType result = 0;')
    func_str_list.append('   DOT: for (int i = 0; i < NUM_FEATURES / PAR_FACTOR_DEC / NUM_OPS; i++){')
    func_str_list.append('#pragma HLS PIPELINE II=1')
    func_str_list.append('   DOT_INNER: for(int j = 0; j < PAR_FACTOR_DEC; j++){')
    func_str_list.append('     FeatureType term = param[i*PAR_FACTOR_DEC+j] * ((FeatureType)feature[i*PAR_FACTOR_DEC+j]);')
    func_str_list.append('     result = result + term;')
    func_str_list.append('   }')
    func_str_list.append('   }')
    func_str_list.append('    out_tmp(31, 0) = result(31, 0); ')
    func_str_list.append('   Output_1.write(out_tmp);')
    func_str_list.append('   odd_even = 1;')
    func_str_list.append('   return;')
    func_str_list.append('  }')
    func_str_list.append('  else{')
    func_str_list.append('   prob(31,0) = Input_2.read();')
    func_str_list.append('   scale = prob - ((FeatureType)training_label);')
    func_str_list.append('')
    func_str_list.append('   GRAD: for (int i = 0; i < NUM_FEATURES / PAR_FACTOR_DEC / NUM_OPS; i++){')
    func_str_list.append('#pragma HLS PIPELINE II=1')
    func_str_list.append('   GRAD_INNER: for (int j = 0; j < PAR_FACTOR_DEC; j++){')
    func_str_list.append('     grad[i*PAR_FACTOR_DEC+j] = (scale * ((FeatureType) feature[i*PAR_FACTOR_DEC+j]));')
    func_str_list.append('   }')
    func_str_list.append('   }')
    func_str_list.append('')
    func_str_list.append('   FeatureType step = STEP_SIZE;')
    func_str_list.append('   UPDATE: for (int i = 0; i < NUM_FEATURES / PAR_FACTOR_DEC / NUM_OPS; i++){')
    func_str_list.append('#pragma HLS PIPELINE II=1')
    func_str_list.append('   UPDATE_INNER: for (int j = 0; j < PAR_FACTOR_DEC; j++){')
    func_str_list.append('    FeatureType tmp;')
    func_str_list.append('    tmp = (-step) * grad[i*PAR_FACTOR_DEC+j];')
    func_str_list.append('    param[i*PAR_FACTOR_DEC+j] = param[i*PAR_FACTOR_DEC+j] + tmp;')
    func_str_list.append('   }')
    func_str_list.append('   }')
    func_str_list.append('')
    func_str_list.append('   num_train++;')
    func_str_list.append('   if(num_train==NUM_TRAINING){')
    func_str_list.append('    num_train = 0;')
    func_str_list.append('    epoch++;')
    func_str_list.append('   }')
    func_str_list.append('   if(epoch==5){')
    func_str_list.append('    STREAM_OUT: for (int i = 0; i < NUM_FEATURES / F_VECTOR_SIZE / NUM_OPS; i ++ ){')
    func_str_list.append('#pragma HLS pipeline II=1')
    func_str_list.append('   bit64 tmp_data1;')
    func_str_list.append('   tmp_data1(31,0) = param[i * F_VECTOR_SIZE + 0](FTYPE_TWIDTH-1, 0);')
    func_str_list.append('   tmp_data1(63,32) = param[i * F_VECTOR_SIZE + 1](FTYPE_TWIDTH-1, 0);')
    func_str_list.append('   Output_2.write(tmp_data1);')
    func_str_list.append('')
    func_str_list.append('    }')
    func_str_list.append('   }')
    func_str_list.append('   odd_even = 0;')
    func_str_list.append('   return;')
    func_str_list.append('  }')
    func_str_list.append('}')

    return 'dotProduct_i'+str(N), "\n".join(func_str_list)

